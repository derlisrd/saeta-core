<style>
    /* Estilos para la transición de las respuestas del FAQ */
    .faq-content {
        max-height: 0;
        overflow: hidden;
        transition: max-height 0.5s ease-out, opacity 0.5s ease-out;
        opacity: 0;
    }
    .faq-content.active {
        max-height: 500px; /* Ajusta este valor si tu contenido es más largo */
        opacity: 1;
    }
</style>

<script>
    document.addEventListener('DOMContentLoaded', function () {
        const faqToggles = document.querySelectorAll('.faq-toggle');

        faqToggles.forEach(button => {
            // Initially hide all content and set aria-expanded to false
            const content = button.nextElementSibling;
            content.classList.add('faq-content'); // Ensure the class is present for initial state
            button.setAttribute('aria-expanded', 'false');

            button.addEventListener('click', () => {
                const content = button.nextElementSibling;
                const chevron = button.querySelector('.chevron');

                // Toggle 'active' class for smooth transition
                content.classList.toggle('active');

                // Rotate chevron and update aria-expanded
                if (content.classList.contains('active')) {
                    chevron.classList.add('rotate-180');
                    button.setAttribute('aria-expanded', 'true');
                } else {
                    chevron.classList.remove('rotate-180');
                    button.setAttribute('aria-expanded', 'false');
                }
            });
        });
    });
</script>